# Receba dois números e realize uma operação simples (adição, subtração, multiplicação ou divisão)

n1 = float(input("Digite o primeiro número: "))
n2 = float(input("Digite o segundo número: "))

soma = n1 + n2
sub = n1 - n2
mult = n1*n2
div = n1/n2

print(f"A soma entre eles é: {soma}")
print(f"A subtração entre eles é: {sub}")
print(f"A multiplicação entre eles é: {mult}")
print(f"A divisão entre eles é: {div}")
